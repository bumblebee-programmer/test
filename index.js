function getRandomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

console.log(getRandomInt(1, 10));

console.log(getRandomInt(1, 10));

function sumOf(a, b) {
  return a + b;
}

console.log(sumOf(1, 2));

